count = 1

while count <= 5:
    print("Count:", count)
    count += 1

print("Loop finished!")

'''
This code initializes a variable count to 1 and then enters a while loop. 
Inside the loop, it prints the current value of count and increments it by 1. 
The loop continues as long as count is less than or equal to 5. 
Once the condition is no longer true, it prints "Loop finished!" and exits the loop.

'''